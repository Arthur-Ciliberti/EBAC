package org.example;

public class RotinaAcordar {

    public static void main(String[] args) {
        boolean alarmeTocando = true;

        if (alarmeTocando) {
            // Passo 2: Abrir os olhos
            System.out.println("Abrindo os olhos...");

            // Passo 3: Desligar o alarme
            System.out.println("Desligando o alarme...");

            // Passo 4: Espreguiçar-se
            System.out.println("Espreguiçando-se...");

            // Passo 5: Sentar na cama
            System.out.println("Sentando na cama...");

            // Passo 6: Esperar alguns segundos para ajustar o corpo
            System.out.println("Aguardando alguns segundos para despertar...");

            // Passo 7: Levantar-se da cama
            System.out.println("Levantando-se da cama...");

            // Passo 8: Calçar os chinelos
            System.out.println("Calçando os chinelos...");

            // Passo 9: Caminhar até o banheiro
            System.out.println("Caminhando até o banheiro...");

            // Passo 10: Lavar o rosto para despertar
            System.out.println("Lavando o rosto...");

            // Passo 11: Escovar os dentes
            System.out.println("Escovando os dentes...");

            // Passo 12: Prosseguir com as demais atividades matinais
            System.out.println("Iniciando a rotina matinal...");
        } else {
            // Se o alarme não estiver tocando, permanecer dormindo
            System.out.println("O alarme não tocou. Continuando dormindo...");
        }
    }
}
